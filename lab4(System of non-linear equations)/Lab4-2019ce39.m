format long;
f = @(x)[(x(1)^2/(180^2))-(x(2)^2/300^2-180^2)-1;(x(2)-500)^2/279^2-(x(1)-300)^2/(500^2-279^2)-1]
%disp(f);
Df = @(x)[(2*x(1))/(180^2),(-2*x(2)/(300^2));((2*(x(1)-300)))/(500^2 -279^2),(2*(x(2)-500))/(279)^2]
%disp(Df);
x = [0.5;0.5]
n = 5;
for i = 1 : n
    Dx = -Df(x)\f(x);
    x = x + Dx;
    f(x);
end   
disp(x);

plot(x,f,x,Df)